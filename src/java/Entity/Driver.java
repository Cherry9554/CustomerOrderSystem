public class Driver {
    private String id;
    private String name;
    private String car_plate;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCar_plate() {
        return car_plate;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setCar_plate(String car_plate) {
        this.car_plate = car_plate;
    }
    
}
